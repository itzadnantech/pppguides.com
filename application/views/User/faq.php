<h3 class="subheading1" style="position: relative;top: 23px; width: 34%; ">PPP loan forgiveness application</h3>
<div class="border justify">
<p>
Unless you subscribe to our White Glove services, you will complete your PPP loan forgiveness application online to determine the amount of your loan that may be eligible for forgiveness. 
</p>

<p>When you calculate your loan forgiveness application through PPPGuides, you are submitting the equivalent of the U.S. Small Business Administration (SBA)'s PPP loan forgiveness application. This is not in lieu of submitting the application to your lender or to the SBA. We may monitor your application details, and may contact you should we discover any issues or information that may benefit your application.
</p>
<p>This application will take one or more hours to complete. As long as you save your work in progress, we save your progress as you go, and you may leave and come back to complete the application in multiple sessions.</p>
<h4 class="subheading" >When can I submit my forgiveness application?
</h4>
<ul>
<li>You are eligible to start filling out your loan application anytime, but you have at least until the end of 2020 to complete it. We recommend you check with your lender as well as SBA website to verify last acceptable date to submit your forgiveness application. Keep in mind that you may apply for forgiveness of eligible expenses paid or incurred during your Covered or Alternate Period. Your Covered Period for loan forgiveness is either: (1) the 24-week (168-day) period beginning on your PPP Loan Disbursement Date, or (2) if you received your PPP loan before June 5, 2020, you may elect to use an eight-week (56-day) Covered Period starting on your PPP Loan Disbursement Date.
</li>
</ul>

<h4 class="subheading">What types of expenses are eligible for forgiveness?
</h4>
<ul>
<li>To receive full loan forgiveness, you must use at least 60% of the PPP loan for payroll costs, and not more than 40% of the loan forgiveness amount may be attributable to eligible non-payroll costs. If you spent less than 60% of your loan proceeds on payroll costs, however, you may be eligible for partial forgiveness of qualifying expenses.
</li></ul>








<h4 class="subheading">The following types of expenses are eligible for forgiveness per SBA guidelines:
</h4>
<ul>
<li>Payroll costs paid or incurred during your Covered Period including cash compensation (e.g., salary, wages, tips, commissions, paid leave) and other benefits (e.g. contributions to employee health insurance, employee retirement plans, and state and local taxes).
</li>

<li>Nonpayroll costs paid or incurred during your Covered Period, including rent expenses with agreements signed prior to February 15, 2020; utility expenses with contracts in force prior to February 15, 2020 (e.g., electricity, gas, water, telephone, or internet access); and mortgage interest payments for properties with mortgage agreements signed prior to February 15, 2020.
</li>

</ul>



<h4 class="subheading">What factors might reduce my loan forgiveness amount?
</h4>
<p>You may experience reductions in your eligible forgiveness amount if:</p>
<ul>
<li>Less than 60% of your loan went to payroll costs.
</li>
<li>Employees' salaries or wages were reduced by over 25%, unless an exception applies.
</li>
<li>The average weekly number of full-time equivalent (FTE) employees decreased, unless an exception applies.
</li>
</ul>



